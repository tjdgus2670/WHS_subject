import uuid

"""
Mock Payment Gateway
=====================
원래는 Visa/Mastercard 같은 카드사 API나 토스페이먼츠·이니시스 같은 PG(결제대행)사 API를
직접 연동하려고 했다. 하지만 개인/학습 프로젝트 수준에서는:

  - 카드사/PG API는 사업자 등록 + 가맹점 계약 + 심사를 거쳐야 키가 발급되는 구조라
    개인이 접근하기 어렵다.
  - 카드번호 같은 민감정보를 직접 다루려면 PCI-DSS 같은 카드정보 보안 표준을 신경 써야
    하는데, 학습 프로젝트에서 이걸 제대로 지키는 건 범위를 벗어난다.
  - 설령 테스트 키가 있어도 계약/심사 절차를 학교 과제 일정 안에 밟기는 비현실적이다.

그래서 이 모듈은 실제 카드사와 통신하지 않고, 서버 내부에서 승인/거절을 흉내내는
Mock Payment Gateway로 대체했다. 다만 구조(요청 -> 승인 처리 -> 거래번호 발급)는
실제 PG 연동 흐름을 최대한 비슷하게 맞춰서, 나중에 실제 API로 교체하기 쉽게 만들었다.

실제 서비스(당근페이 등)에서는 보통 PG사에 결제를 위임하는 구조를 쓴다. 카드 원본
번호는 PG사 서버에만 존재하고(토큰화), 가맹점 서버는 토큰과 승인 결과만 받는다.
이 Mock Gateway도 그 원칙만은 실제처럼 지킨다: 카드번호를 어디에도 저장하지 않고,
승인 처리 후 즉시 버린다.
"""


class PaymentDeclinedError(Exception):
    """결제가 거절되었을 때 발생시킨다."""
    pass


def charge(amount: int, card_number: str) -> str:
    """Mock으로 결제를 승인 처리하고, 가상의 결제(거래) 번호를 반환한다.

    카드번호는 이 함수 안에서만 잠깐 쓰이고 반환값에도, 어디에도 저장되지 않는다.
    테스트 편의를 위해 카드번호가 '0000'으로 끝나면 일부러 거절되도록 만들어뒀다
    (결제 실패 케이스를 테스트할 수 있어야 하므로).
    """
    normalized = card_number.replace(" ", "")

    if normalized.endswith("0000"):
        raise PaymentDeclinedError("카드 승인이 거절되었습니다. 다른 카드로 시도해주세요.")

    if amount <= 0:
        raise PaymentDeclinedError("결제 금액이 올바르지 않습니다.")

    # 실제 PG라면 여기서 카드사에 승인 요청을 보내고 거래번호(TID)를 받아온다.
    # 여기서는 서버가 임의로 발급한다.
    return f"mock_{uuid.uuid4().hex[:20]}"
